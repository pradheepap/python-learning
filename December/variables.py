## No need to declare the variables. They
### are created when the value is assigned

number = 4
helloString = "Hello Hi!!!"

print (number)
print (helloString)
 

 # Explicitly assign the type of the variable

number = int(6)
helloString = str ("Its me")

print (number)
print (helloString)

